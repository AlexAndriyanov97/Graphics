package ru.nsu.fit.g16208.andriyanov.graphics.model;

public class FieldOfDefinition {

    private int a,b,c,d;

    public FieldOfDefinition(int a, int b, int c, int d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public int GetWidth(){
        return Math.abs(a-b);
    }

    public int GetHeight(){
        return Math.abs(c-d);
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }

    public int getD() {
        return d;
    }


    public void setA(int a) {
        this.a = a;
    }

    public void setB(int b) {
        this.b = b;
    }

    public void setC(int c) {
        this.c = c;
    }

    public void setD(int d) {
        this.d = d;
    }
}
